function buildEvidenceText(analysis: TruthGuardAnalysis, t: typeof TRANSLATIONS.en): string {
  const lines: string[] = [
    t.analysisHeader,
    '',
    `${t.input}: ${analysis.originalInput}`,
    `${t.type}: ${analysis.inputType}`,
    `${t.scamScore}: ${analysis.scamProbability}%`,
    `${t.verdict}: ${analysis.verdict ?? analysis.seniorModeVerdict ?? 'N/A'}`,
    `${t.timestamp}: ${new Date().toLocaleString()}`,
    '',
    `${t.riskSignals}:`,
  ];

  if (analysis.riskSignals && analysis.riskSignals.length > 0) {
    analysis.riskSignals.forEach((signal) => {
      lines.push(`  [${signal.level}] ${signal.type}: ${signal.evidence}`);
    });
  } else {
    lines.push('  (none)');
  }

  lines.push('');
  lines.push(t.footer);
  return lines.join('\n');
}

const EvidencePack: React.FC<EvidencePackProps> = ({
  analysis,
  language = 'en',
  isSeniorMode = false,
}) => {
  const [copied, setCopied] = useState(false);
  const t = TRANSLATIONS[language as keyof typeof TRANSLATIONS] ?? TRANSLATIONS.en;

  const evidenceText = buildEvidenceText(analysis, t);

  const handleCopy = async () => {
    try {
      await navigator.clipboard.writeText(evidenceText);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch {
      // Fallback: select text
    }
  };

  const handleDownload = () => {
    const blob = new Blob([evidenceText], { type: 'text/plain;charset=utf-8' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `verifyfirst-evidence-${Date.now()}.txt`;
    a.click();
    URL.revokeObjectURL(url);
  };

  return (
    <div className={`border border-gray-700 rounded-2xl ${isSeniorMode ? 'p-6 mb-8' : 'p-5 mb-6'} bg-gray-900/40`}>
      <h3 className={`font-bold text-white mb-1 flex items-center gap-2 ${isSeniorMode ? 'text-2xl' : 'text-lg'}`}>
        📁 {t.title}
      </h3>
      <p className={`text-gray-400 mb-4 ${isSeniorMode ? 'text-lg' : 'text-sm'}`}>{t.subtitle}</p>

      <pre className={`bg-gray-950 rounded-lg p-3 text-gray-400 font-mono whitespace-pre-wrap break-all mb-4 overflow-auto max-h-48 ${isSeniorMode ? 'text-sm' : 'text-xs'}`}>
        {evidenceText}
      </pre>

      <div className="flex gap-3 flex-wrap">
        <button
          onClick={handleCopy}
          className={`flex items-center gap-2 font-semibold rounded-xl transition-colors ${
            copied
              ? 'bg-green-700 text-white'
              : 'bg-gray-700 hover:bg-gray-600 text-gray-200'
          } ${isSeniorMode ? 'px-6 py-3 text-lg' : 'px-4 py-2 text-sm'}`}
        >
          {copied ? <CheckCircle2 className="w-4 h-4" /> : <Copy className="w-4 h-4" />}
          {copied ? t.copied : t.copy}
        </button>
        <button
          onClick={handleDownload}
          className={`flex items-center gap-2 bg-gray-700 hover:bg-gray-600 text-gray-200 font-semibold rounded-xl transition-colors ${isSeniorMode ? 'px-6 py-3 text-lg' : 'px-4 py-2 text-sm'}`}
        >
          <Download className="w-4 h-4" />
          {t.download}
        </button>
      </div>
    </div>
  );
};

export default EvidencePack;
